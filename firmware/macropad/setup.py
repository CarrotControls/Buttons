CABLE_RIGHT_MAP = (0x3, 0x7, 0xB, 0xF, 0x2, 0x6, 0xA, 0xE, 0x1, 0x5, 0x9, 0xD, 0x0, 0x4, 0x8, 0xC) # confirmed

keyMapping = CABLE_LEFT_MAP

pixels = neopixel.NeoPixel(pixel_pin, num_pixels, brightness=0.1, auto_write=False, pixel_order=ORDER)

